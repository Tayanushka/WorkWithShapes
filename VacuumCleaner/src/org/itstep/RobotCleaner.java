package org.itstep;

public class RobotCleaner {
	private String model;
	private int spendSecondsToClean;

	public int getSpendSecondsToClean() {
		return spendSecondsToClean;
	}

	public void setSpendSecondsToClean(int spendSecondsToClean) {
		this.spendSecondsToClean = spendSecondsToClean;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}
}
